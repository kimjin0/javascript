class Person {
    constructor(name, age) {
        this.name = name;
        this.age = age;
    }
    speak() {
        console.log(`${this.name} hi`);
    }
}

const kimjin0 = new Person("kimjin0", 42);
console.log(kimjin0.name);
console.log(kimjin0.age);
kimjin0.speak();

