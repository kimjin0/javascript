function clickLogo(){
  let item1 = document.getElementsByClassName("sh");
  let item2 = document.getElementsByClassName("pa");
  let item3 = document.getElementsByClassName("sk");

  for(let i=0; i<item1.length; i++){
    item1[i].parentElement.style.display = 'flex';
  }
  for(let i=0; i<item2.length; i++){
    item2[i].parentElement.style.display = 'flex';
  }
  for(let i=0; i<item3.length; i++){
    item3[i].parentElement.style.display = 'flex';
  }
}
